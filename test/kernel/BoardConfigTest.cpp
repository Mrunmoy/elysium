class BoardConfigTest : public ::testing::Test
{
protected:
    void SetUp() override
    {
        board::configReset();
    }
};

// ---- Identity ----

TEST_F(BoardConfigTest, BoardName)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    EXPECT_STREQ(board::config().boardName, "STM32F407ZGT6");
}

TEST_F(BoardConfigTest, Mcu)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    EXPECT_STREQ(board::config().mcu, "STM32F407ZGT6");
}

TEST_F(BoardConfigTest, Arch)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    EXPECT_STREQ(board::config().arch, "cortex-m4");
}

// ---- Clocks ----

TEST_F(BoardConfigTest, SystemClock)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    EXPECT_EQ(board::config().systemClock, 168000000u);
}

TEST_F(BoardConfigTest, Apb1Clock)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    EXPECT_EQ(board::config().apb1Clock, 42000000u);
}

TEST_F(BoardConfigTest, Apb2Clock)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    EXPECT_EQ(board::config().apb2Clock, 84000000u);
}

TEST_F(BoardConfigTest, HseClock)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    EXPECT_EQ(board::config().hseClock, 8000000u);
}

// ---- Memory regions ----

TEST_F(BoardConfigTest, MemoryRegionCount)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    EXPECT_EQ(board::config().memoryRegionCount, 3u);
}

TEST_F(BoardConfigTest, FlashRegion)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    ASSERT_GE(board::config().memoryRegionCount, 1u);
    EXPECT_STREQ(board::config().memoryRegions[0].name, "flash");
    EXPECT_EQ(board::config().memoryRegions[0].base, 0x08000000u);
    EXPECT_EQ(board::config().memoryRegions[0].size, 0x100000u);
}

TEST_F(BoardConfigTest, SramRegion)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    ASSERT_GE(board::config().memoryRegionCount, 2u);
    EXPECT_STREQ(board::config().memoryRegions[1].name, "sram");
    EXPECT_EQ(board::config().memoryRegions[1].base, 0x20000000u);
    EXPECT_EQ(board::config().memoryRegions[1].size, 0x20000u);
}

TEST_F(BoardConfigTest, CcmRegion)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    ASSERT_GE(board::config().memoryRegionCount, 3u);
    EXPECT_STREQ(board::config().memoryRegions[2].name, "ccm");
    EXPECT_EQ(board::config().memoryRegions[2].base, 0x10000000u);
    EXPECT_EQ(board::config().memoryRegions[2].size, 0x10000u);
}

// ---- Console ----

TEST_F(BoardConfigTest, ConsoleUart)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    EXPECT_STREQ(board::config().consoleUart, "usart1");
}

TEST_F(BoardConfigTest, ConsoleBaud)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    EXPECT_EQ(board::config().consoleBaud, 115200u);
}

TEST_F(BoardConfigTest, ConsoleTxPin)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    EXPECT_TRUE(board::config().hasConsoleTx);
    EXPECT_EQ(board::config().consoleTx.port, 'A');
    EXPECT_EQ(board::config().consoleTx.pin, 9u);
    EXPECT_EQ(board::config().consoleTx.af, 7u);
}

TEST_F(BoardConfigTest, ConsoleRxPin)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    EXPECT_TRUE(board::config().hasConsoleRx);
    EXPECT_EQ(board::config().consoleRx.port, 'A');
    EXPECT_EQ(board::config().consoleRx.pin, 10u);
    EXPECT_EQ(board::config().consoleRx.af, 7u);
}

// ---- LED ----

TEST_F(BoardConfigTest, LedPresent)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    EXPECT_TRUE(board::config().hasLed);
    EXPECT_EQ(board::config().led.port, 'C');
    EXPECT_EQ(board::config().led.pin, 13u);
}

// ---- Features ----

TEST_F(BoardConfigTest, FpuPresent)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    EXPECT_TRUE(board::config().hasFpu);
}

// ---- consoleUartId mapping ----

TEST_F(BoardConfigTest, ConsoleUartIdMapping)
{
    board::configInit(kF407Dtb, kF407DtbSize);
    EXPECT_EQ(board::consoleUartId(), hal::UartId::Usart1);
}

// ---- Invalid DTB ----

TEST_F(BoardConfigTest, InvalidDtb_DoesNotCrash)
{
    std::uint8_t bad[4] = {0, 0, 0, 0};
    board::configInit(bad, sizeof(bad));
    // Config should be zeroed
    EXPECT_EQ(board::config().systemClock, 0u);
    EXPECT_EQ(board::config().boardName, nullptr);
}
